'use strict';

export default function (arr: Array<any>) {
  return flat(arr, []);
};

function flat(arr: Array,  res: Array) {
  var i = 0, cur;
  var len = arr.length;
  for (; i < len; i++) {
    cur = arr[i];
    Array.isArray(cur) ? flat(cur, res) : res.push(cur);
  }
  return res;
}